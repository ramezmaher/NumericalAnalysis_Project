% --- Executes on button press in Interpolate.
function Interpolate_Callback(hObject, eventdata, handles)
% hObject    handle to Interpolate (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
data = get(handles.Table_Points, 'Data');
X = data(:, 1);
Y = data(:, 2);
Val = get(handles.Method_Type, 'Value');
R = 0;
tic;
if(Val == 1)
    R = NewtonInterpolation(X, Y);
else
    R = LagrangeInterpolation(X,Y);
end
elapsed = toc;
set(handles.Execution_Time, 'String', num2str(elapsed));
Func = matlabFunction(R);
x2 = (min(X)-1):0.1:(max(X)+1);
y2 = Func(x2);
axes(handles.Graph);
hold off;
plot(X, Y, 'o');
hold on;
plot(x2, y2);
xlabel('x Axis');
ylabel('F(x) Axis');
legend('Input point', 'Fitting curve');
set(handles.Function, 'String', char(R));


